/**
 * Recursively replaces every string literal in `T` with `string` so
 * other translation files can populate the same shape with different
 * values without TypeScript treating each English string as a literal.
 */
type Loosen<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends object
      ? Loosen<T[K]>
      : T[K];
};

export type Translations = Loosen<typeof en>;
